package com.xuexibao.teacher.model;

public class TeacherChoose {

	private String teacherId;
	private String learnPhase;
	private Integer realSubject;
	
	public String getTeacherId() {
		return teacherId;
	}
	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}
	public String getLearnPhase() {
		return learnPhase;
	}
	public void setLearnPhase(String learnPhase) {
		this.learnPhase = learnPhase;
	}
	public Integer getRealSubject() {
		return realSubject;
	}
	public void setRealSubject(Integer realSubject) {
		this.realSubject = realSubject;
	}
	
	
}
